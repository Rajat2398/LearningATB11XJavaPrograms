package ex_28_Wrapper_Class;

public class Lab212_Primitive_Wrapper {
    public static void main(String[] args) {


        int a = 10;//Primitive Data types
        //This is not object
        //It will not have attribute or behaviour


//Wrapper-in programming it is a class that encapsulates or "Wraps" a primitive data types
//into an object

//Allowing primitive data types to be treated as objects
//->Attributes
//->Behaviour


//A wrapper class is a class whoes objects wraps  or contains primitive data types

//We will use like this-Integer, Character, Float, etc.

        Integer age = 10;
        System.out.println(age.intValue());
        System.out.println(Integer.MIN_VALUE);
        System.out.println(Integer.MAX_VALUE);
        System.out.println(age);


    }


}


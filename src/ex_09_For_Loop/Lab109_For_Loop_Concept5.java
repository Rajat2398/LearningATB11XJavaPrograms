package ex_09_For_Loop;

public class Lab109_For_Loop_Concept5 {
    public static void main(String[] args) {
        for (int i=0;i<10;i++){


            System.out.println(i);//0 to 9=total time->10
        }

        for (int j=1;j<=10;j++){


            System.out.println(j);// //1 to 10= total time->10
        }
    }
}

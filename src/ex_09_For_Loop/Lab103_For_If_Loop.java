package ex_09_For_Loop;

public class Lab103_For_If_Loop {
    public static void main(String[] args) {
        for(int s=0; s<18; s++){


            if (s>15){


                System.out.println("Gift received");

            }else{
                System.out.println("No gift");
            }
        }
    }
}
// From 0 to 15 condition "No gift" will print
//and for 16 and 17 condition "Gift received" will print